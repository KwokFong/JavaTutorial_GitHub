package javatutorial01;

public class Application {

	public static void main(String[] args) {
		System.out.println("Hello World!!!");
		
		// Eight Primitive Types 
		int myNumber = 88;
		myNumber = 88;
		short myShort = 848;
		long myLong = 9797;
		
		double myDouble = 7.32423;
		float myFloat = 5.3432f;
		char myChar = 'A';
		boolean myBoolean = true;
		byte myByte = 127;
		
		System.out.println("myNumber of int = " + myNumber);
		System.out.println("myShort of short = " + myShort);
		System.out.println("myLong of long = " + myLong);
		System.out.println("myDouble of double = " + myDouble);
		System.out.println("myFloat of float = " + myFloat);
		
		System.out.println("myChar of char = " + myChar);
		System.out.println("myBoolean of boolean = " + myBoolean);
		System.out.println("myByte of byte = " + myByte);
		
		
	}

}
